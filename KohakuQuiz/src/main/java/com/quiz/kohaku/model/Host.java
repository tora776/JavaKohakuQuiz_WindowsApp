package com.quiz.kohaku.model;

public class Host {
	private int participation_id;
	private String host_name;
	private int year;
	private int appearance;
	private String host_role;
	
	public int getParticipation_id() {
		return participation_id;
	}
	public void setParticipation_id(int participation_id) {
		this.participation_id = participation_id;
	}
	public String getHost_name() {
		return host_name;
	}
	public void setHost_name(String host_name) {
		this.host_name = host_name;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getAppearance() {
		return appearance;
	}
	public void setAppearance(int appearance) {
		this.appearance = appearance;
	}
	public String getHost_role() {
		return host_role;
	}
	public void setHost_role(String host_role) {
		this.host_role = host_role;
	}
	
	
}
